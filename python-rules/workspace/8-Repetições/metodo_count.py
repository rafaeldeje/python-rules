lista_numeros = [1, 2, 3, 2, 4, 2, 5, 2]

# Contando o número de ocorrências do número 2 na lista
ocorrencias_numero_2 = lista_numeros.count(2)

# Imprimindo o número de ocorrências
print(f"O número de ocorrências do número 2 na lista é: {ocorrencias_numero_2}")

#este exemplo, lista_numeros.count(2) retorna o número de vezes que o valor 2 ocorre na lista. O resultado é então impresso na tela. Este método é útil quando você precisa determinar quantas vezes um determinado elemento aparece em uma lista.
