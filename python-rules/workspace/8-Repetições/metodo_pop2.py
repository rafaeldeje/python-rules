lista_frutas = ['maçã', 'banana', 'laranja', 'morango']

print("Lista original:", lista_frutas)

# Especificando a posição (índice) do elemento a ser removido
posicao = 2

fruta_removida = lista_frutas.pop(posicao)

print(f"Fruta removida da posição {posicao}: {fruta_removida}")
print("Lista após a remoção:", lista_frutas)
