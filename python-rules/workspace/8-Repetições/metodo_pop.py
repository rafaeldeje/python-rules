#pop é utilizado para remover e retornar o último elemento de uma lista

lista_animais = ['Cachorro', 'Gato', 'Passaro', 'Peixe']

print("Lista original: ", lista_animais)

ultimo_animal = lista_animais.pop()

print("Ultimo animal removido: ", ultimo_animal)
print("Lista apos a remocao: ", lista_animais)
