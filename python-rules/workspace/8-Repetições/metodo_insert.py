#Para inserir um novo item na posição dada é o insert

lista_num = [1, 2, 4, 5]

print("Lista original: ", lista_num)

lista_num.insert(2,3)

print("Lista após a inserção: ", lista_num)

#O método insert(2, 3) insere o número 3 na posição de índice 2, deslocando os elementos subsequentes para a direita. O resultado final é [1, 2, 3, 4, 5].
